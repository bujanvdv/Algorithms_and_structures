# Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.

print('Побитовые операции над числами 5 и 6.')
a = 5
b = 6

print('Операция И:', a & b)

print('Операция ИЛИ:', a | b)

print('Сдвиг 5 влево на 2 знака:', a << 2)

print('Сдвиг 5 вправо на 2 знака:', a >> 2)
